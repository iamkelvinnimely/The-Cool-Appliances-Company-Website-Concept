// Helper function to get all products across all categories
export const getAllProducts = () => {
  return Object.values(productCategories).flatMap(category => category.products);
};

// Helper function to get products by category
export const getProductsByCategory = (categorySlug) => {
  return productCategories[categorySlug]?.products || [];
};

// Helper function to get a single product by ID
export const getProductById = (id) => {
  const allProducts = getAllProducts();
  return allProducts.find(product => product.id === parseInt(id));
};

// Helper function to get all categories
export const getAllCategories = () => {
  return Object.keys(productCategories).map(slug => ({
    slug,
    name: productCategories[slug].name,
    productCount: productCategories[slug].products.length
  }));
};
